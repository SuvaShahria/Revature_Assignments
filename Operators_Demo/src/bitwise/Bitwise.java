package bitwise;

public class Bitwise {

	public static void main(String[] args) {
 
        int a = 5, b = 6; 
       
        System.out.println("bitwise and, 5&6 = " + (a & b)); 
  
   
        System.out.println("bitwise or,5|6 = " + (a | b)); 
  

        System.out.println("bitwise xor, 5^6 = " + (a ^ b)); 
  
	}

}
